#include<iostream>
using namespace std;
int main()
{
    int a=100;

    if(a==20)
    {
        cout<<"A is equal to 20"<<endl;
    }
    else if(a==30)
    {
        cout<<"A is equal to 30"<<endl;
    }
    else if(a==40)
    {
        cout<<"A is equal to 40"<<endl;
    }
    else
    {
        cout<<"Value does not match"<<endl;
    }
    cout<<"Exact Value of a is :"<<a<<endl;
    return 0;
}
